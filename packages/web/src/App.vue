<template>
  <MainLayout>
    <!-- Ê†áÈ¢òÊèíÊßΩ -->
    <template #title>
      Prompt Optimizer
    </template>

    <!-- Êìç‰ΩúÊåâÈíÆÊèíÊßΩ -->
    <template #actions>
      <ActionButton
        icon="üìù"
        text="ÂäüËÉΩÊèêÁ§∫ËØç"
        @click="openTemplateManager('optimize')"
      />
      <ActionButton
        icon="üìú"
        text="ÂéÜÂè≤ËÆ∞ÂΩï"
        @click="showHistory = true"
      />
      <ActionButton
        icon="‚öôÔ∏è"
        text="Ê®°ÂûãÁÆ°ÁêÜ"
        @click="showConfig = true"
      />
    </template>

    <!-- ‰∏ªË¶ÅÂÜÖÂÆπÊèíÊßΩ -->
    <!-- ÊèêÁ§∫ËØçÂå∫ -->
    <ContentCard>
      <!-- ËæìÂÖ•Âå∫Âüü -->
      <div class="flex-none">
        <InputPanel
          v-model="prompt"
          v-model:selectedModel="selectedOptimizeModel"
          label="ÂéüÂßãÊèêÁ§∫ËØç"
          placeholder="ËØ∑ËæìÂÖ•ÈúÄË¶Å‰ºòÂåñÁöÑprompt..."
          model-label="‰ºòÂåñÊ®°Âûã"
          template-label="‰ºòÂåñÊèêÁ§∫ËØç"
          button-text="ÂºÄÂßã‰ºòÂåñ ‚Üí"
          loading-text="‰ºòÂåñ‰∏≠..."
          :loading="isOptimizing"
          :disabled="isOptimizing"
          @submit="handleOptimizePrompt"
          @configModel="showConfig = true"
        >
          <template #model-select>
            <ModelSelect
              ref="optimizeModelSelect"
              :modelValue="selectedOptimizeModel"
              @update:modelValue="selectedOptimizeModel = $event"
              :disabled="isOptimizing"
              @config="showConfig = true"
            />
          </template>
          <template #template-select>
            <TemplateSelect
              v-model="selectedOptimizeTemplate"
              type="optimize"
              @manage="openTemplateManager('optimize')"
              @select="handleTemplateSelect"
            />
          </template>
        </InputPanel>
      </div>

      <!-- ‰ºòÂåñÁªìÊûúÂå∫Âüü -->
      <div class="flex-1 min-h-0 overflow-y-auto">
        <PromptPanel 
          v-model:optimized-prompt="optimizedPrompt"
          :is-iterating="isIterating"
          v-model:selected-iterate-template="selectedIterateTemplate"
          :versions="currentVersions"
          :current-version-id="currentVersionId"
          @iterate="handleIteratePrompt"
          @openTemplateManager="openTemplateManager"
          @switchVersion="handleSwitchVersion"
        />
      </div>
    </ContentCard>

    <!-- ÊµãËØïÂå∫Âüü -->
    <ContentCard>
      <!-- ÊµãËØïËæìÂÖ•Âå∫Âüü -->
      <div class="flex-none">
        <InputPanel
          v-model="testContent"
          v-model:selectedModel="selectedTestModel"
          label="ÊµãËØïÂÜÖÂÆπ"
          placeholder="ËØ∑ËæìÂÖ•Ë¶ÅÊµãËØïÁöÑÂÜÖÂÆπ..."
          model-label="Ê®°Âûã"
          button-text="ÂºÄÂßãÊµãËØï ‚Üí"
          loading-text="ÊµãËØï‰∏≠..."
          :loading="isTesting"
          :disabled="isTesting"
          @submit="() => handleTest(optimizedPrompt)"
          @configModel="showConfig = true"
        >
          <template #model-select>
            <ModelSelect
              ref="testModelSelect"
              :modelValue="selectedTestModel"
              @update:modelValue="selectedTestModel = $event"
              :disabled="isTesting"
              @config="showConfig = true"
            />
          </template>
        </InputPanel>
      </div>

      <!-- ÊµãËØïÁªìÊûúÂå∫Âüü -->
      <div class="flex-1 min-h-0 overflow-y-auto">
        <OutputPanel
          ref="outputPanelRef"
          :loading="isTesting"
          :error="testError"
          :result="testResult"
        />
      </div>
    </ContentCard>

    <!-- ÂºπÁ™óÊèíÊßΩ -->
    <template #modals>
      <!-- ÈÖçÁΩÆÂºπÁ™ó -->
      <Teleport to="body">
        <ModelManager
          v-if="showConfig"
          @close="handleModelManagerClose"
          @modelsUpdated="handleModelsUpdated"
          @select="handleModelSelect"
        />
      </Teleport>

      <!-- ÊèêÁ§∫ËØçÁÆ°ÁêÜÂºπÁ™ó -->
      <Teleport to="body">
        <TemplateManager
          v-if="showTemplates"
          :template-type="currentType"
          :selected-optimize-template="selectedOptimizeTemplate"
          :selected-iterate-template="selectedIterateTemplate"
          @close="handleTemplateManagerClose"
          @select="handleTemplateSelect"
        />
      </Teleport>

      <!-- ÂéÜÂè≤ËÆ∞ÂΩïÂºπÁ™ó -->
      <HistoryDrawer
        v-model:show="showHistory"
        :history="history"
        @reuse="handleSelectHistory"
        @clear="handleClearHistory"
      />
    </template>
  </MainLayout>
</template>

<script setup>
import '@prompt-optimizer/ui/style.css'
import { ref, onMounted, watch } from 'vue'
import { 
  createLLMService, 
  createPromptService,
  modelManager,
  templateManager,
  historyManager
} from '@prompt-optimizer/core'
import {
  Toast,
  ModelManager,
  OutputPanel,
  PromptPanel,
  TemplateManager,
  TemplateSelect,
  ModelSelect,
  HistoryDrawer,
  InputPanel,
  MainLayout,
  ContentCard,
  ActionButton,
  usePromptOptimizer,
  usePromptTester,
  useToast
} from '@prompt-optimizer/ui'
import { v4 as uuidv4 } from 'uuid'

// ÂàùÂßãÂåñÊúçÂä°
const llmService = createLLMService(modelManager)
const promptServiceRef = ref(null)

// ÂàùÂßãÂåñ toast
const toast = useToast()

// Áä∂ÊÄÅ
const showConfig = ref(false)
const showHistory = ref(false)
const showTemplates = ref(false)
const currentType = ref('optimize')  // ÈªòËÆ§‰∏∫‰ºòÂåñÊèêÁ§∫ËØç
const history = ref([])
const outputPanelRef = ref(null)

// Ê∑ªÂä† ref
const optimizeModelSelect = ref(null)
const testModelSelect = ref(null)

// ÂàùÂßãÂåñÁªÑÂêàÂºèÂáΩÊï∞
const {
  prompt,
  optimizedPrompt,
  isOptimizing,
  isIterating,
  selectedOptimizeTemplate,
  selectedIterateTemplate,
  selectedOptimizeModel,
  selectedTestModel,
  currentVersions,
  currentVersionId,
  currentChainId,
  handleOptimizePrompt,
  handleIteratePrompt,
  handleSwitchVersion,
  saveTemplateSelection,
  initTemplateSelection,
  handleModelSelect,
  initModelSelection,
  loadModels
} = usePromptOptimizer(modelManager, templateManager, historyManager, promptServiceRef)

const {
  testContent,
  testResult,
  testError,
  isTesting,
  handleTest
} = usePromptTester(promptServiceRef, selectedTestModel)

// ÂàùÂßãÂåñ promptService
const initServices = async () => {
  try {
    promptServiceRef.value = await createPromptService(modelManager, llmService)
  } catch (error) {
    console.error('ÊúçÂä°ÂàùÂßãÂåñÂ§±Ë¥•:', error)
    toast.error('ÊúçÂä°ÂàùÂßãÂåñÂ§±Ë¥•')
  }
}

const handleSelectHistory = (context) => {
  const { record, chainId, rootPrompt } = context;
  
  // ËÆæÁΩÆÂéüÂßãÊèêÁ§∫ËØç
  prompt.value = rootPrompt;
  // ËÆæÁΩÆ‰ºòÂåñÂêéÁöÑÊèêÁ§∫ËØç
  optimizedPrompt.value = record.optimizedPrompt;
  
  // ÂàõÂª∫Êñ∞ÁöÑchain
  const newRecord = historyManager.createNewChain({
    id: uuidv4(),
    originalPrompt: rootPrompt,
    optimizedPrompt: record.optimizedPrompt,
    type: 'optimize',
    modelKey: record.modelKey,
    templateId: record.templateId,
    timestamp: Date.now(),
    metadata: {}
  });
  
  // Êõ¥Êñ∞ÂΩìÂâçchain‰ø°ÊÅØ
  currentChainId.value = newRecord.chainId;
  currentVersions.value = newRecord.versions;
  currentVersionId.value = newRecord.currentRecord.id;
  
  // Êõ¥Êñ∞ÂéÜÂè≤ËÆ∞ÂΩï
  history.value = historyManager.getAllChains();
  
  showHistory.value = false;
}

// Ê∑ªÂä†Ê∏ÖÁ©∫ÂéÜÂè≤ËÆ∞ÂΩïÁöÑÂ§ÑÁêÜÂáΩÊï∞
const handleClearHistory = async () => {
  try {
    await historyManager.clearHistory()
    history.value = []
    console.log('ÂéÜÂè≤ËÆ∞ÂΩïÂ∑≤Ê∏ÖÁ©∫')
    toast.success('ÂéÜÂè≤ËÆ∞ÂΩïÂ∑≤Ê∏ÖÁ©∫')
  } catch (error) {
    console.error('Ê∏ÖÁ©∫ÂéÜÂè≤ËÆ∞ÂΩïÂ§±Ë¥•:', error)
    toast.error('Ê∏ÖÁ©∫ÂéÜÂè≤ËÆ∞ÂΩïÂ§±Ë¥•')
  }
}

// ‰øÆÊîπÊèêÁ§∫ËØçÈÄâÊã©Â§ÑÁêÜÂáΩÊï∞
const handleTemplateSelect = async (template, type) => {
  // Ëé∑ÂèñÊúÄÊñ∞ÁöÑÊ®°ÊùøÊï∞ÊçÆ
  const updatedTemplate = template ? await templateManager.getTemplate(template.id) : null
  
  if (type === 'optimize') {
    selectedOptimizeTemplate.value = updatedTemplate
  } else {
    selectedIterateTemplate.value = updatedTemplate
  }
  
  await saveTemplateSelection(updatedTemplate, type)
  toast.success(`Â∑≤ÈÄâÊã©${type === 'optimize' ? '‰ºòÂåñ' : 'Ëø≠‰ª£'}ÊèêÁ§∫ËØç: ${updatedTemplate?.name || 'Êó†'}`)
}

// ÊâìÂºÄÊèêÁ§∫ËØçÁÆ°ÁêÜÂô®
const openTemplateManager = (type = 'optimize') => {
  currentType.value = type
  showTemplates.value = true
}

const loadTemplates = async () => {
  try {
    // Á°Æ‰øùÊ®°ÊùøÁÆ°ÁêÜÂô®ÈáçÊñ∞ÂàùÂßãÂåñ
    await templateManager.init()
    // ÈáçÊñ∞ÂàùÂßãÂåñÊ®°ÊùøÈÄâÊã©
    await initTemplateSelection()
    
    // ÂêåÊ≠•ÂΩìÂâçÈÄâ‰∏≠ÁöÑÊ®°Êùø
    if (selectedOptimizeTemplate.value) {
      const template = await templateManager.getTemplate(selectedOptimizeTemplate.value.id)
      if (template) {
        selectedOptimizeTemplate.value = template
      }
    }
    if (selectedIterateTemplate.value) {
      const template = await templateManager.getTemplate(selectedIterateTemplate.value.id)
      if (template) {
        selectedIterateTemplate.value = template
      }
    }
    
    toast.success('ÊèêÁ§∫ËØçÂàóË°®Â∑≤Êõ¥Êñ∞')
  } catch (error) {
    console.error('Âä†ËΩΩÊèêÁ§∫ËØçÂ§±Ë¥•:', error)
    toast.error('Âä†ËΩΩÊèêÁ§∫ËØçÂ§±Ë¥•')
  }
}

// ‰øÆÊîπ handleTemplateManagerClose ÊñπÊ≥ï
const handleTemplateManagerClose = async () => {
  // ÂÖàÊõ¥Êñ∞Êï∞ÊçÆ
  await loadTemplates()
  // ÊúÄÂêéÂÖ≥Èó≠ÁïåÈù¢
  showTemplates.value = false
}

// ‰øÆÊîπÊ®°ÂûãÁÆ°ÁêÜÂô®ÂÖ≥Èó≠Â§ÑÁêÜÂáΩÊï∞
const handleModelManagerClose = async () => {
  // ÂÖàÊõ¥Êñ∞Êï∞ÊçÆ
  await loadModels()
  // Âà∑Êñ∞Ê®°ÂûãÈÄâÊã©ÁªÑ‰ª∂
  optimizeModelSelect.value?.refresh()
  testModelSelect.value?.refresh()
  // ÂÖ≥Èó≠ÁïåÈù¢
  showConfig.value = false
}

// ‰øÆÊîπÊ®°ÂûãÊõ¥Êñ∞Â§ÑÁêÜÂáΩÊï∞
const handleModelsUpdated = (modelKey) => {
  // Â¶ÇÊûúÈúÄË¶ÅÔºåÂèØ‰ª•Âú®ËøôÈáåÂ§ÑÁêÜÊ®°ÂûãÊõ¥Êñ∞ÂêéÁöÑÂÖ∂‰ªñÈÄªËæë
}

// ÁîüÂëΩÂë®ÊúüÈí©Â≠ê
onMounted(async () => {
  await initServices()
  await initModelSelection()
  
  // ÂàùÂßãÂåñÂéÜÂè≤ËÆ∞ÂΩïÁÆ°ÁêÜÂô®
  try {
    console.log('ÂàùÂßãÂåñÂéÜÂè≤ËÆ∞ÂΩïÁÆ°ÁêÜÂô®...')
    await historyManager.init()
    history.value = historyManager.getAllChains()
    console.log('ÂéÜÂè≤ËÆ∞ÂΩïÂä†ËΩΩÂÆåÊàê:', {
      recordCount: history.value?.length,
      chains: history.value?.map(chain => ({
        chainId: chain.chainId,
        versionsCount: chain.versions.length,
        rootRecord: chain.rootRecord,
        currentRecord: chain.currentRecord
      }))
    })
  } catch (error) {
    console.error('Âä†ËΩΩÂéÜÂè≤ËÆ∞ÂΩïÂ§±Ë¥•:', error)
    toast.error('Âä†ËΩΩÂéÜÂè≤ËÆ∞ÂΩïÂ§±Ë¥•')
  }
  
  // ÂàùÂßãÂåñÊèêÁ§∫ËØçÈÄâÊã©
  await initTemplateSelection()
})

// Ê∑ªÂä†ÂéÜÂè≤ËÆ∞ÂΩïÊòæÁ§∫Áä∂ÊÄÅÁõëÂê¨
watch(showHistory, (newVal) => {
  if (newVal) {
    // ÊâìÂºÄÂéÜÂè≤ËÆ∞ÂΩïÊó∂ÔºåÈáçÊñ∞Ëé∑ÂèñÊúÄÊñ∞Êï∞ÊçÆ
    history.value = historyManager.getAllChains()
  }
  console.log('ÂéÜÂè≤ËÆ∞ÂΩïÊòæÁ§∫Áä∂ÊÄÅÂèòÊõ¥:', {
    show: newVal,
    currentHistory: history.value?.length
  })
})

// ÁõëÂê¨‰ºòÂåñÂÆåÊàêÔºåÊõ¥Êñ∞ÂéÜÂè≤ËÆ∞ÂΩï
watch([currentVersions], () => {
  history.value = historyManager.getAllChains()
})
</script>

